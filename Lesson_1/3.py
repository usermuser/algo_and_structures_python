# 3.	По введенным пользователем координатам двух точек вывести
# уравнение прямой вида y = kx + b, проходящей через эти точки.

x1 = 4.3 # float(input('x1 = '))
y1 = -1.2 # float(input('y1 = '))

x2 = -8.5 # int(input('x1 = '))
y2 = 4 # int(input('y1 = '))

k = (y2-y1)/(x2-x1)
b = y1 - k*x1

print(f'y = {k:.2f}x + {b:.2f}')
